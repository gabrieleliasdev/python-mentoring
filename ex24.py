pessoas = 20
gatos = 30
cachorros = 15

if pessoas < gatos:
  print('Muitos gatos irão explodir.')

if pessoas > gatos:
  print('O mundo está salvo.')

if pessoas < cachorros:
  print('O mundo está todo babado.')

if pessoas > cachorros:
  print('O mundo está seco.')

cachorros += 5

if pessoas >= cachorros:
  print('Pessoas são maiores ou iguais aos cachorros.')

if pessoas <= cachorros:
  print('Pessoas são menores ou iguais aos cachorros.')

if pessoas == cachorros:
  print('Pessoas são cachorros.')
